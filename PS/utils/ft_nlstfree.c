#include "../push_swap.h"

void	ft_nlstfree(t_nlist	**a)
{
	t_nlist	*n;
	t_nlist	*l;

	l = *a;
	while (l)
	{
		n = l->next;
		if (l)
			free(l);
		l = NULL;
		l = n;
	}
}
